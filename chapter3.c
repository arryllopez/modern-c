// c has 5 conditional control statements being if for do while and switch 

// the if construct looks like 
if (i > 25) { 
    j = i - 25; 
}

// we can add the else if and else constructs to handle multiple conditions

if (i > 25) { 
    j = i - 25; 
} else if (i < 25) { 
    j = i + 25; 
} else { 
    j = 0; 
}


